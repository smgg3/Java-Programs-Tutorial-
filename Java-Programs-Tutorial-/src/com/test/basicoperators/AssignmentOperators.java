package com.test.basicoperators;

public class AssignmentOperators {

	public static void main(String[] args) {
		int a=190138;
		int b=5;
		int c;
		c=a;
		c+=a;
		System.out.println(c);
		c-=b;
		c*=a;
		c/=a;
		c%=a;
		c|=a;
		c&=a;
		c^=a;
		c<<=a;
		c>>=a;
		
		System.out.println(c);

	}

}
